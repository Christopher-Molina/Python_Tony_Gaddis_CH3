# Input number in the range 1 through 7
number = int(input('Enter number in the range 1 through 7: '))

# Decision structure
if number == 1:
   print('Monday')
elif number == 2:
   print('Tuesday')
elif number == 3:
   print('Wednesday')
elif number == 4:
   print('Thursday')
elif number == 5:
   print('Friday')
elif number == 6:
   print('Saturday')
elif number == 7:
   print('Sunday')
else:
   print('Error, enter a valid number in the range 1 through 7')